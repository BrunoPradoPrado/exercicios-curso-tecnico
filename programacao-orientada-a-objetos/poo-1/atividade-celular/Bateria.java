
public class Bateria {
	public int capacidade;

	public Bateria() {
		this.capacidade=0;
	}
	public Bateria(int capacidade) {
		this.capacidade=capacidade;
	}
	
	public int getCapacidade() {
		return capacidade;
	}

	public void setCapacidade(int capacidade) {
		this.capacidade = capacidade;
	}
	
	
}
